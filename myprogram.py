def print_tree_names():
    trees = [
        "Oak",
        "Maple",
        "Pine",
        "Birch",
        "Cedar",
        "Spruce",
        "Willow",
        "Redwood",
        "Palm",
        "Baobab"
    ]

    print("List of 10 Tree Names:")
    for tree in trees:
        print(f"- {tree}")

if __name__ == "__main__":
    print_tree_names()
